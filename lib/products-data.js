export function getCategoryById(id) {
  const aliases = {
    'heatset-web-fountain-concentrates': 'coldset-fountain-concentrates',
  }

  return categories.find((c) => c.id === (aliases[id] || id))
}

export function getProductByCode(code) {
  for (const cat of categories) {
    const product = cat.products.find((p) => p.code === code)
    if (product) return { ...product, category: cat }
  }
  return null
}

export function getRelatedProducts(categoryId, excludeCode, limit = 3) {
  const cat = categories.find((c) => c.id === categoryId)
  if (!cat) return []
  return cat.products
    .filter((p) => p.code !== excludeCode)
    .slice(0, limit)
    .map((p) => ({ ...p, category: cat }))
}

